export function getProducts(filters = {}) {
  let result = [...products]

  if (filters.category) {
    if (filters.category === 'racoes') {
      result = result.filter(p => p.category === 'caes' || p.category === 'gatos')
    } else {
      result = result.filter(p => p.category === filters.category)
    }
  }

  if (filters.featured) {
    result = result.filter(p => p.featured)
  }

  if (filters.q) {
    const search = filters.q.toLowerCase()
    result = result.filter(p =>
      p.name.toLowerCase().includes(search) ||
      p.brand.toLowerCase().includes(search) ||
      p.description.toLowerCase().includes(search)
    )
  }

  if (filters.sort) {
    const sortField = filters.sort.startsWith('-') ? filters.sort.slice(1) : filters.sort
    const sortOrder = filters.sort.startsWith('-') ? -1 : 1
    result.sort((a, b) => {
      if (sortField === 'price') return (a.price - b.price) * sortOrder
      if (sortField === 'name') return a.name.localeCompare(b.name) * sortOrder
      return 0
    })
  }

  if (filters.limit) {
    result = result.slice(0, filters.limit)
  }

  return result
}

export function getProductById(id) {
  return products.find(p => p.id === parseInt(id))
}

export function calculateFreight(cep) {
  const cepNum = parseInt(cep)
  let region = 'sudeste'

  if (cepNum >= 1000 && cepNum <= 19999) region = 'sp'
  else if (cepNum >= 20000 && cepNum <= 39999) region = 'sudeste'
  else if (cepNum >= 40000 && cepNum <= 65999) region = 'nordeste'
  else if (cepNum >= 66000 && cepNum <= 69999) region = 'norte'
  else if (cepNum >= 70000 && cepNum <= 76999) region = 'centroOeste'
  else if (cepNum >= 77000 && cepNum <= 77999) region = 'norte'
  else if (cepNum >= 78000 && cepNum <= 79999) region = 'centroOeste'
  else if (cepNum >= 80000 && cepNum <= 99999) region = 'sul'

  const freightTable = {
    sp: { basePrice: 15.90, daysMin: 2, daysMax: 4 },
    sudeste: { basePrice: 22.90, daysMin: 3, daysMax: 6 },
    sul: { basePrice: 25.90, daysMin: 4, daysMax: 7 },
    centroOeste: { basePrice: 29.90, daysMin: 5, daysMax: 8 },
    nordeste: { basePrice: 34.90, daysMin: 6, daysMax: 10 },
    norte: { basePrice: 39.90, daysMin: 8, daysMax: 15 },
  }

  const freight = freightTable[region]

  return {
    options: [
      { name: 'PAC', price: freight.basePrice, days: `${freight.daysMin} a ${freight.daysMax}` },
      { name: 'SEDEX', price: freight.basePrice * 1.8, days: `${Math.max(1, freight.daysMin - 1)} a ${freight.daysMax - 2}` }
    ]
  }
}
